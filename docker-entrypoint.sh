#!/bin/sh
# Точка входа контейнера бота: при каждом запуске сначала прогоняем миграции,
# затем стартуем самого бота. Если миграции упадут — контейнер не стартует,
# и это видно в логах (restart: unless-stopped попробует снова).
set -e

echo "[entrypoint] Проверяю и применяю миграции Alembic…"
alembic upgrade head
echo "[entrypoint] Миграции в актуальном состоянии. Запускаю бота…"

exec python -m app.main
